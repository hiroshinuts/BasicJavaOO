/*
 * Declaracao de uma função ou Metodo
 * 
 * [modificador] [tiṕo] nome() {
 *   // corpo
 * };
 * 
 *  ***************** Uso obrigatorio de () depois do nome, para Funcoes e metodo***************
 *  **************** Isso diferencia de uma variavel*******************************************
 *  
 *  EX - void metodo(){
 *  
 *  }
 * 
 */



public class FuncoesEMetodos {
	
	
	public static void metodo() { //minimo para declarar um metodo , ainda falta o modificador de acesso
		
		System.out.println("Impresso através de um Metodo");
		
	}
	
	public static void main(String[] args) {
		
		metodo(); // invocando o metodo
		
		
		
		
	}
	

}
